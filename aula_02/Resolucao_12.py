# Exercício 12 - Positivo, negativo ou zero
numero = int(input("Digite um número: ")) # Recebe um número.
if numero > 0: # Verifica se é maior que zero.
    print("Positivo") # Mostra positivo.
elif numero < 0: # Verifica se é menor que zero.
    print("Negativo") # Mostra negativo.
else: # Caso não seja maior nem menor.
    print("Zero") # Mostra zero.