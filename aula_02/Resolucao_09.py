# Exercício 9 - Senha simples
senha_correta = "123456" # Guarda a senha correta.
senha = input("Digite a senha: ") # Recebe a senha digitada.
if senha == senha_correta: # Compara as duas senhas.
    print("Acesso liberado") # Executa se forem iguais.
else: # Executa se forem diferentes.
    print("Acesso negado") # Mostra acesso negado.