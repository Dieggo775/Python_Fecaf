# Exercício 8 - Temperatura
temperatura = float(input("Digite a temperatura: ")) # Recebe a temperatura.
if temperatura > 30: # Verifica se está acima de 30.
    print("Está calor") # Mostra mensagem de calor.
else: # Caso seja 30 ou menos.
    print("Temperatura agradável") # Mostra mensagem alternativa