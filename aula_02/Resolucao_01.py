# Exercício 1 - Maioridade
idade = int(input("Digite sua idade: ")) # Recebe a idade e converte para inteiro.
if idade >= 18: # Verifica se a idade é maior ou igual a 18.
    print("Maior de idade") # Executa se a condição for verdadeira.
else: # Executa se a condição for falsa.
    print("Menor de idade") # Mostra que é menor de idade.