# Exercício 2 - Pode dirigir
idade = int(input("Digite sua idade: ")) # Recebe a idade.
if idade >= 18: # Verifica se tem idade mínima para dirigir.
    print("Pode dirigir") # Mensagem para quem pode dirigir.
else: # Caso não tenha 18 anos.
    print("Não pode dirigir") # Mensagem para quem ainda não pode dirigir.