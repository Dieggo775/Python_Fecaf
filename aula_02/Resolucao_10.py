# Exercício 10 - Frequência escolar
frequencia = float(input("Digite a frequência: ")) # Recebe a frequência.
if frequencia >= 75: # Verifica se atingiu o mínimo.
    print("Aprovado por frequência") # Mostra aprovado.
else: # Caso esteja abaixo de 75.
    print("Reprovado por frequência") # Mostra reprovado.