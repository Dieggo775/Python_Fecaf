# Exercicio 6 - Hora Extra
horas = float(input("Digite as horas trabalhadas: ")) # Recebe as horas trablhadas
if horas > 8: # Verifica se passou de 8 horas
    print("Fez hora extra") # Mostra que fez hora extra
else: # Caso trabalhe 8 horas ou menos
    print("Não faz hora extra") # Mostra que não fez hora extra